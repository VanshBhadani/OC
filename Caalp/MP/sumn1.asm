  MOV DS,AX
  LEA DX,NL1
  MOV AH,09H
  INT 21H
  MOV AH,02H
  INT 21H
  SUB AL,30H
  MOV CL,AL
  MOV ANS,0
LOOP1:
  LEA DX,NL2
  MOV AH,09H
  INT 21H
  MOV AH,02H
  INT 21H
  SUB AL,30H
  ADD ANS,AL
