ASSUME CS:CODE,DS:DATA
DATA SEGMENT
ARR DB 3,5,7,1,9
LEN DB 4H
NL1 DB 10,"Enter number to search:$"
NL2 DB 10,"FOUND$"
NL3 DB 10,"NOT FOUND$"
DATA ENDS
CODE SEGMENT
START:
   MOV AX,DATA
   MOV DS,AX
   LEA DX,NL1
   MOV AH,09H
   INT 21H
   MOV AH,01H
   INT 21H
   SUB AL,30H
   MOV CL,LEN
   MOV SI,OFFSET ARR
LOOP1:
   CMP AL,[SI]
   JE LOOP2
   INC SI
   LOOP LOOP1
   LEA DX,NL3
   MOV AH,09H
   INT 21H
   MOV AH,4CH
   INT 21H
LOOP2:
   LEA DX,NL2
   MOV AH,09H
   INT 21H
   MOV AH,4CH
   INT 21H
CODE ENDS
END START
   

   