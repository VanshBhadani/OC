ASSUME CS:CODE,DS:DATA
DATA SEGMENT
ARR DB 3,5,7,2,8,1
LEN DW $-ARR
MAX DB 0
MIN DB 0
NL1 DB 10,"MIN:$"
NL2 DB 10,"MAX:$"
DATA ENDS
CODE SEGMENT
START:
   MOV AX,DATA
   MOV DS,AX
   MOV SI,OFFSET[ARR]
   MOV AL,[SI]
   MOV MIN,AL
   MOV MAX,AL
   MOV CX,LEN
   DEC CX
LOOP1:
   INC SI
   MOV AL,[SI]
   CMP MIN,AL
   JAE LOOP2
   MOV MIN,AL
LOOP2:
   CMP MAX,AL
   JLE LOOP3
   MOV MAX,AL
LOOP3:
   LOOP LOOP1
   LEA DX,NL1
   MOV AH,09H
   INT 21H
   MOV DL,MIN
   ADD DL,30H
   MOV AH,02H
   INT 21H
   LEA DX,NL2
   MOV AH,09H
   INT 21H
   MOV DL,MAX
   ADD DL,30H
   MOV AH,02H
   INT 21H
   MOV AH,4CH
   INT 21H
CODE ENDS
END START

   
   