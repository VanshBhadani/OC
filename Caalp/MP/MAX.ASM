CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
MOV SI,OFFSET LIST
MOV CL, COUNT
MOV AL, [SI]
AGAIN:
CMP AL,[SI+1]
JNL NEXT
MOV AL,[SI+1]
NEXT:
INC SI
DEC CL
CMP CL,0H
JG AGAIN
MOV MAX, AL
ADD AL, 30H
MOV DL, AL
MOV AH, 02H
INT 21H
MOV AH,4CH
INT 21H
CODE ENDS
END START
