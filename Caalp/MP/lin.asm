ASSUME CS:CODE,DS:DATA
DATA SEGMENT
ARR DB 1H,2H,3H,4H,5H
COUNT DB 5H
NL1 DB 10,"ENTER ELEMENT TO SEARCH:$"
NL2 DB 10,"FOUND$"
NL3 DB 10,"NOT FOUND$"
DATA ENDS
CODE SEGMENT
START:
    MOV AX,DATA
    MOV DS,AX
    LEA DX,NL1
    MOV AH,09H
    INT 21H
    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV CL,COUNT
    MOV SI,OFFSET ARR
FIRST:
    CMP AL,[SI]
    JE NEXT
    INC SI
    LOOP FIRST
    LEA DX,NL3
    MOV AH,09H
    INT 21H
    MOV AH,4CH
    INT 21H
NEXT:
    LEA DX,NL2
    MOV AH,09H
    INT 21H
    MOV AH,4CH
    INT 21H
CODE ENDS
END START

    
    
    