ASSUME CS:CODE,DS:DATA
DATA SEGMENT
LIST DB 1,2,3,4,5,6,7 
LEN DW $-LIST
MAX DB ?
MIN DB ?
MSG1 DB 10,"MAX:$"
MSG2 DB 10,"MIN:$"
DATA ENDS
CODE SEGMENT
START:
    MOV AX,DATA
    MOV DS,AX
    MOV SI,OFFSET LIST
    MOV AL,[SI]
    MOV MIN,AL
    MOV MAX,AL
    MOV CX,LEN
    DEC CX
FIRST:
    INC SI
    MOV AL,[SI]
    CMP AL,MIN
    JAE SECOND
    MOV MIN,AL
SECOND:
    CMP AL,MAX
    JBE LAST
    MOV MAX,AL
LAST:
    LOOP FIRST
    LEA DX,MSG1
    MOV AH,09H
    INT 21H
    MOV DL,MIN
    ADD DL,30H
    MOV AH,02H
    INT 21H
    LEA DX,MSG2
    MOV AH,09H
    INT 21H
    MOV DL,MAX
    ADD DL,30H
    MOV AH,02H
    INT 21H
    
    MOV AH,4CH
    INT 21H

CODE ENDS
END START
    
