ASSUME CS:CODE,DS:DATA 
DATA SEGMENT
STR DB "COMPUTER$"
LEN DW $-STR
REV DB 20 DUP(' ')
DATA ENDS
CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
MOV CX,LEN
ADD CX,-2
MOV SI,OFFSET STR
MOV DI,OFFSET REV
ADD SI,LEN
ADD SI,-2
LOOP1:
MOV AL,[SI]
MOV [DI],AL
DEC SI
INC DI
LOOP LOOP1
MOV AL,[SI]
MOV [DI],AL
INC DI
MOV DL,'$'
MOV [DI],DL
LEA DX,REV
MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H
CODE ENDS
END START